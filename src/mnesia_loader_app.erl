-module('mnesia_loader_app').

-behaviour(application).

%% Application callbacks
-export([start/2
        ,stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    case mnesia_loader:start_mnesia() of
		ok ->
			mnesia_loader_sup:start_link();
		E ->
			E
	end.

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

